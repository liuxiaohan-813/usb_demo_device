/* dev Configurations */
const unsigned char ConDes[9 + 9 + 9 + 7]=
{
    // Configuation Descriptor
    0x09, // bLength
    0x02, // bDescpriptorType
    sizeof(ConDes) & 0xFF,
    (sizeof(ConDes)) >> 8 & 0xFF, // wTotalLength
    0x01, // bNumInterfaces
    0x01, // bConfigurationValue
    0x00, // iConfiguration
    0x80, // bmAttributes
    0x32, // bMaxPower : 100mA

    // Interface
    0x09, // bLength
    0x04, // bDescriptorType
    0x00, // bInterfaceNumber
    0x00, // bAlternateSetting
    0x01, // bNumEndpoints(not include Ep0)
    0x03, // bInterfaceClass (Hid)
    0x01, // bInterfaceSubClass
    0x01, // bInterfaceProtocol : keyboard
    0x00, // iInterface
    // conf HID descriptor
    0x09, // bLength
    0x21, // bDescriptorType
    0x11,
    0x01, // bcdHID : 0x0111
    0x00, // bContryCode
    0x01, // bNumDescriptor
    0x22, // bDescriptorType
    sizeof(ReportDescriptor) & 0xFF,
    (sizeof(ReportDescriptor) >> 8) & 0xFF, // wDescriptorLength
    // Endpoint
    0x07, // bLength
    0x05, // bDescriptorType
    0x81, // Ep1 : In
    0x03, // bmAttributes : Interrupt
    0x08,
    0x00, // wMaxPackeSize : 0x0008
    0xA  // bInterval : 10ms
};

